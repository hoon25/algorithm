package grammer;

/*
Primitive, Reference Array InitValue 확인
 */
public class PrimitveAndReferenceArray {
    public static void main(String[] args) {
        int[] intArray = new int[1];
        String[] stringArray = new String[1];

        System.out.println("intArray[0] = " + intArray[0]); // 0
        System.out.println("stringArray[0] = " + stringArray[0]); // null
    }
}
